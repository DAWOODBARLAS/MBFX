            500, 1000, 1500, 2400, 4500, 6000, 7500, 10000, 12000, 14000, 16000,
            18000, 20000,
        ];

        rangeselider2.addEventListener("input", updateselider2Value);

        function updateselider2Value() {
            const value = rangeselider2.value;
            const formattedValue1 = `${randomValues1[value - 1]}`;
            const formattedValue2 = `${randomValues2[value - 1]}`;

            selider2Value1.textContent = formattedValue1;
            selider2Value2.textContent = formattedValue2;

            // Move the input thumb value display to the thumb
            rangeselider2.style.setProperty("--value", `'${value}'`);
        }
