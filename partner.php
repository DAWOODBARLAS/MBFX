        function toggleAdditionalText() {
            var additionalText = document.getElementById('additionalText');
            additionalText.style.visibility = (additionalText.style.visibility === 'hidden' || additionalText.style.visibility === '') ? 'visible' : 'hidden';
        }

        document.getElementById('info-icons').addEventListener('click', function() {
            toggleAdditionalTexts();
        });

        function toggleAdditionalTexts() {
            var additionalTexts = document.getElementById('additionalTexts');
            additionalTexts.style.visibility = (additionalTexts.style.visibility === 'hidden' || additionalTexts.style.visibility === '') ? 'visible' : 'hidden';
        }

        const rangeSlider = document.getElementById("rangeSlider");
        const sliderValues = document.getElementById("sliderValues");
        const sliderValue = document.getElementById("sliderValue");


        rangeSlider.addEventListener("input", updateSliderValue);

        function updateSliderValue() {
            const value = rangeSlider.value;
            const formattedValue = `${value}$`;
            const thumbPosition =
                ((value - rangeSlider.min) / (rangeSlider.max - rangeSlider.min)) *
                100;

            sliderValue.textContent = formattedValue;
            sliderValues.style.left = `calc(${thumbPosition}% - 15px)`; // Adjust the offset for better positioning

            // Move the input thumb value display to the thumb
            rangeSlider.style.setProperty("--value", `'${formattedValue}'`);
        }


        /// Second slider/////
        const rangeselider2 = document.getElementById("rangeselider2");
        const selider2Values1 = document.getElementById("selider2Values1");
        const selider2Values2 = document.getElementById("selider2Values2");
        const selider2Value1 = document.getElementById("selider2Value1");
        const selider2Value2 = document.getElementById("selider2Value2");

        // Array of 13 random values for the first section
        const randomValues1 = [
            150, 350, 500, 800, 1500, 2000, 2500, 3000, 3500, 4000, 4500, 5000,
